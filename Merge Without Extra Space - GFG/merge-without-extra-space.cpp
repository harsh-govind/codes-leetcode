class Solution{
    public:
        //Function to merge the arrays.
        void merge(long long a[], long long b[], int m, int n) 
        { 
            int left = m-1, right=0;
            while(left>= 0 and right<n)
            {
                if(a[left]>b[right])
                {
                    swap(a[left], b[right]);
                    right++;
                    left--;
                }
                else
                {
                    break;
                }
            }
            
            sort(a, a+m);
            sort(b, b+n);
        } 
};

